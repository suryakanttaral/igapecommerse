var express = require("express");
var State = require("../../models/shared/State");
const router = express.Router();

router.post("/save", async (req, res) => {
  let body = req.body;
  let state = new State.State();
  state.id = body.data.id;
  state.name = body.data.name;
  state.save().then(
    (result) => {
      let data = {
        data: {
          status: "success",
          data: result,
        },
      };
      res.end(JSON.stringify(data));
    },
    (err) => {
      let data = {
        data: {
          status: "fail",
        },
      };
      res.end(JSON.stringify(data));
    }
  );
});


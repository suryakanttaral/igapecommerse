var express = require("express");
var Admin = require("../../models/igap/Admin");
const router = express.Router();

router.post("/save", async (req, res) => {
  let body = req.body;
  let admin = new Admin.Admin();
  admin.id = body.data.id;
  admin.name = body.data.name;
  admin.username = body.data.username;
  admin.password = body.data.password;
  admin.firebaseid = body.data.firebaseid;
  admin.save().then(
    (result) => {
      let data = {
        data: {
          status: "success",
          data: result,
        },
      };
      res.end(JSON.stringify(data));
    },
    (err) => {
      let data = {
        data: {
          status: "fail",
        },
      };
      res.end(JSON.stringify(data));
    }
  );
});

